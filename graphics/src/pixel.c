#include <graphics.h>

void	pixel_put(t_key *k, t_lk l)
{
	int		i;

	i = 0;
	if (l.x < WIN_W && l.y < WIN_H && l.x >= 0 && l.y >= 0)
	{
		i = (l.y - 1) * WIN_W + l.x;
		((t_color*)k->img)[i] = k->pix;
	}
}
